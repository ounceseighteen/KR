@app.route("/api/testcases")
def api_testcases():
    module = request.args.get("module", "")
    status = request.args.get("status", "")
    priority = request.args.get("priority", "")
    q = request.args.get("q", "").lower()

    result = [get_tc_with_status(tc) for tc in TEST_CASES]

    if module:
        result = [t for t in result if t["module_code"] == module]
    if status:
        result = [t for t in result if t["status"] == status]
    if priority:
        result = [t for t in result if t["priority"] == priority]
    if q:
        result = [t for t in result if q in t["name"].lower() or q in t["id"].lower()]

    return jsonify(result)

@app.route("/api/testcases/<tc_id>", methods=["GET"])
def api_tc_detail(tc_id):
    tc = next((t for t in TEST_CASES if t["id"] == tc_id), None)
    if not tc:
        return jsonify({"error": "not found"}), 404
    return jsonify(get_tc_with_status(tc))

