# ─── In-memory хранилище статусов ─────────────────────────────────────────
_statuses = {}  # id -> {status, defect, note, updated_at}

def get_tc_with_status(tc):
    s = _statuses.get(tc["id"], {})
    return {**tc,
            "status":     s.get("status", "not_run"),
            "defect":     s.get("defect", ""),
            "note":       s.get("note", ""),
            "updated_at": s.get("updated_at", "")}

