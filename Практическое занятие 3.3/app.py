@app.route("/api/testcases")
def api_testcases():
    module   = request.args.get("module", "")
    status   = request.args.get("status", "")
    priority = request.args.get("priority", "")
    q        = request.args.get("q", "").lower()

    result = [get_tc_with_status(tc) for tc in TEST_CASES]

    if module:   result = [t for t in result if t["module_code"] == module]
    if status:   result = [t for t in result if t["status"] == status]
    if priority: result = [t for t in result if t["priority"] == priority]
    if q:        result = [t for t in result if q in t["name"].lower() or q in t["id"].lower()]

    return jsonify(result)

@app.route("/api/testcases/<tc_id>", methods=["GET"])
def api_tc_detail(tc_id):
    tc = next((t for t in TEST_CASES if t["id"] == tc_id), None)
    if not tc:
        return jsonify({"error": "not found"}), 404
    return jsonify(get_tc_with_status(tc))

@app.route("/api/testcases/<tc_id>/status", methods=["POST"])
def api_update_status(tc_id):
    tc = next((t for t in TEST_CASES if t["id"] == tc_id), None)
    if not tc:
        return jsonify({"error": "not found"}), 404
    data = request.get_json()
    _statuses[tc_id] = {
        "status":     data.get("status", "not_run"),
        "defect":     data.get("defect", ""),
        "note":       data.get("note", ""),
        "updated_at": datetime.now().strftime("%d.%m.%Y %H:%M")
    }
    return jsonify({"ok": True})

@app.route("/api/stats")
def api_stats():
    all_tc = [get_tc_with_status(tc) for tc in TEST_CASES]
    total  = len(all_tc)
    by_status = {}
    for s in ["passed","failed","blocked","skipped","not_run"]:
        by_status[s] = sum(1 for t in all_tc if t["status"] == s)

    by_module = {}
    for t in all_tc:
        m = t["module_code"]
        if m not in by_module:
            by_module[m] = {"name": t["module"], "total":0,"passed":0,"failed":0,"blocked":0,"skipped":0,"not_run":0}
        by_module[m]["total"] += 1
        by_module[m][t["status"]] += 1

    by_priority = {}
    for t in all_tc:
        p = t["priority"]
        if p not in by_priority:
            by_priority[p] = {"total":0,"passed":0,"failed":0}
        by_priority[p]["total"] += 1
        if t["status"] == "passed": by_priority[p]["passed"] += 1
        if t["status"] == "failed": by_priority[p]["failed"] += 1

    executed  = total - by_status["not_run"]
    pass_rate = round(by_status["passed"] / executed * 100, 1) if executed > 0 else 0

    return jsonify({
        "total":      total,
        "by_status":  by_status,
        "by_module":  by_module,
        "by_priority":by_priority,
        "executed":   executed,
        "pass_rate":  pass_rate
    })

@app.route("/api/reset", methods=["POST"])
def api_reset():
    _statuses.clear()
    return jsonify({"ok": True})

